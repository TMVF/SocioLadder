     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $employee = new employee;
        $employee->id = $request->id;
        $employee->CompanyName = $request->CompanyName;
        $employee->Designation = $request->Designation;
        $employee->From = $request->From;
        $employee->To = $request->To;
        $employee->JobType = $request->JobType;
        $employee->ExperienceDetails = $request->ExperienceDetails;
        $employee->SkillName = $request->SkillName;
        $employee->Proficiency = $request->Proficiency;
        $employee->save();
        return view('success');
    }

